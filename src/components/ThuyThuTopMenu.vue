<script lang="ts">
import { defineComponent } from 'vue';

export default defineComponent({
  name: 'ThuyThuTopMenu',
  mounted() {
    // Javascript for mobile menu toggle
    const menuToggle = this.$el.querySelector('.menu-toggle') as HTMLButtonElement | null;
    const mainNav = this.$el.querySelector('.main-nav') as HTMLElement | null;
    if (menuToggle && mainNav) {
      menuToggle.addEventListener('click', () => {
        mainNav.classList.toggle('toggled');
        const isExpanded = mainNav.classList.contains('toggled');
        menuToggle.setAttribute('aria-expanded', isExpanded.toString());
      });
    }

    // Javascript for submenu toggles on mobile
    const dropdownToggles = this.$el.querySelectorAll('.dropdown-menu-toggle');
    dropdownToggles.forEach((toggle: Element) => {
      toggle.addEventListener('click', (event: Event) => {
        event.preventDefault();
        const menuItem = (toggle as HTMLElement).closest('.menu-item-has-children');
        if (menuItem) {
          const subMenu = menuItem.querySelector('.sub-menu') as HTMLElement | null;
          if (subMenu) {
            subMenu.classList.toggle('toggled');
            // Optionally, manage aria-expanded for submenus here as well
            // For example, toggle an aria-expanded attribute on the parent menu item or the toggle itself.
            const isSubMenuExpanded = subMenu.classList.contains('toggled');
            toggle.setAttribute('aria-expanded', isSubMenuExpanded.toString());
            // It might be better to set aria-expanded on the parent `li` or `a` tag if the toggle is just an icon.
          }
        }
      });
    });
  }
});
</script>

